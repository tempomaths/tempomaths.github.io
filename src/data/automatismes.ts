type AutoParams = {
  id: string;
  title: string;
  level: Level;
  progressionStep: ProgressionStep;
  domain: MathDomain;
  subdomain: string;
  difficulty: Automatisme["difficulty"];
  statementTemplate: string;
  answerTemplate: string;
  correctionTemplate: string;
  variablesSchema: VariableSchema;
  duration?: number;
  tags?: string[];
};

const sourceNote =
  "Base initiale inspirée des attendus de collège; à vérifier et enrichir selon la progression de l'enseignant.";

function auto(params: AutoParams): Automatisme {
  return {
    id: params.id,
    title: params.title,
    levels: [params.level],
    progressionStep: params.progressionStep,
    domain: params.domain,
    subdomain: params.subdomain,
    difficulty: params.difficulty,
    statementTemplate: params.statementTemplate,
    answerTemplate: params.answerTemplate,
    correctionTemplate: params.correctionTemplate,
    variablesSchema: params.variablesSchema,
    defaultDurationSeconds: params.duration ?? 30,
    tags: params.tags ?? [],
    sourceNote,
    status: "official"
  };
}

